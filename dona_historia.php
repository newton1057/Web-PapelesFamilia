                <h5><b>Conserva adecuadamente tus archivos</b></h5>
                <p>El lugar donde guardan los papeles es de vital importancia para su preservación. Evita el calor, la humedad y la luz. El calor seca y debilita el papel, el moho puede destruir el papel con el tiempo, y la luz borrar los escritos y las imágenes.</p>
                <h6><b>Recomendaciones:</b></h6>
                <ul>
                    <li>Guarda los papeles en un lugar seco y tibio, en donde no varíe la temperatura y no haya humedad. (las azoteas y sótanos no son buenos lugares, debido a las altas temperaturas y humedad)</li>
                    <li>Mantén los papeles lejos de la luz, dentro de una caja de plástico o metal. Un clóset oscuro y sin humedad puede ser un buen lugar para guardarlos.</li>
                    <li>Te recomendamos no utilizar clips ni grapas para organizar tus documentos. El óxido que suelen generar con el tiempo este tipo de materiales es altamente peligroso para materiales como el papel. En su lugar te recomendamos, meter en bolsas de plástico, cajas, folders o sobres.</li>
